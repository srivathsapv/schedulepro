    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int l,r;
        
        if((!jTextField1.getText().equals("") && !Validation.isNumber(jTextField1.getText())) || (!jTextField2.getText().equals("") && !Validation.isNumber(jTextField2.getText()))){
            JOptionPane.showMessageDialog(null,"Numeric Value Required");
            return;
        }
        if(jTextField1.getText().equals("")) l=1;
        else l = Integer.parseInt(jTextField1.getText());
        
        if(jTextField2.getText().equals("")) r=1;
        else r = Integer.parseInt(jTextField2.getText());
        
        ResultSet cnt_rs = Utilfunctions.executeQuery("SELECT COUNT(*) FROM classschedule WHERE classCode = '" + classCode + "'");
        try {
            cnt_rs.next();
            if(cnt_rs.getInt(1) >= 1){
                int n = Utilfunctions.executeUpdate("UPDATE classschedule SET lunchPeriods = " + l + ",recessPeriods = " + r + " WHERE classCode = " + classCode);
            }
            else {
                int n = Utilfunctions.executeUpdate("INSERT INTO classschedule VALUES(" + classCode + "," + l + "," + r + ")");
            }
            JOptionPane.showMessageDialog(null,"Saved");
            this.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(ClassScheduleDetailsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

